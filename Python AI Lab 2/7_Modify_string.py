
#Upper Case
# The upper() method returns the string in upper case:
a = "s m asiful islam saky"
print(a.upper())

#Lower Case
# The lower() method returns the string in upper case:
a = "S M ASIFUL ISLAM SAKY"
print(a.lower())

#Remove Whitespace
# The strip() method returns the string in upper case:
a = "  S M SAKY  "
print(a.strip())

