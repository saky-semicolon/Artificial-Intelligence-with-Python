
# You can use the 'in' keyword to check if a string is present in another string-

string1 = "Hello World"
print("Hello" in string1)

if "World" in string1:
  print("Yes, 'World' is present in the string1")       #Yes, 'World' is present in the string1

# To check if a certain phrase or character is NOT present in a string, we can use the keyword not in.

lan = "Python is a high level language"
print("expensive" not in lan)

if "expensive" not in lan:
  print("No, 'extensive' is not present in the lan.")
